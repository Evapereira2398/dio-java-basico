package operadores;

public class opUnariosInc {
    public static void main(String[] args) {
        // Incrementando um valor do N° 
        int numeroincr = 5; 
        
        // Para adicionarmos apenas um número ao valor que o definido na variável, utilizamos
        numeroincr ++;
        System.out.println(numeroincr);

        // Tambem podemos utilizar 
        //"System.out.println( ++ numeroincr);"
        
        //Incrementa "+ 2" ao valor definido a variável
        //numeroincr = numeroincr + 2;
    }
}
