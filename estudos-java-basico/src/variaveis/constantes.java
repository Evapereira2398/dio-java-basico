package variaveis;

public class constantes {
     public static void main(String[] args) {
    
        // Alterando o valor de uma variável 
        int numero = 5; 
        numero = 10; 

        System.out.println(numero); 

        //Definindo valores fixos para variável
        // Adicionamos no começo da variável o palabra "final", assim o valor atribuido a ela se torna fixo

        final int numerofixo = 75; 
        
        System.out.print(numerofixo);


    }
    
}
